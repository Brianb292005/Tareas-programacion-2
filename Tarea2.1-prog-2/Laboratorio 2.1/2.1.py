
# Declaracion de variables #
a = 13
b = 10
c = 150

# Operaciones matematicas #

suma = a + b
resta = b - a * c
multiplicacion = a * b / c
division = c / a + b

Todo = suma + resta + multiplicacion + division

print("El resultado de todas las operaciones es:", Todo)

# Comparaciones #
comparacion1 = a > b
comparacion2 = c <= b
comparacion3 = (a + b) == c

print("Comparacion 1 (a > b):", comparacion1)
print("Comparacion 2 (c <= b):", comparacion2)
print("Comparacion 3 ((a + b) == c):", comparacion3)

# expresiones lógicas #
logica1 = (a < b) and (c > a)
logica2 = (b == 10) or (c < 100)
logica3 = not (a >= b)

print("Logica 1 ((a < b) and (c > a)):", logica1)
print("Logica 2 ((b == 10) or (c < 100)):", logica2)
print("Logica 3 (not (a >= b)):", logica3)

